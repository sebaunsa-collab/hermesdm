# Helper: build a standard monster dict
def _m(
    name: str,
    size: str,
    creature_type: str,
    alignment: str,
    ac: int,
    hp_max: int,
    hp_formula: str,
    speed: int,
    fly_speed: int = 0,
    swim_speed: int = 0,
    climb_speed: int = 0,
    STR: int = 10, DEX: int = 10, CON: int = 10,
    INT: int = 10, WIS: int = 10, CHA: int = 10,
    saving_throws: dict | None = None,
    skills: dict | None = None,
    damage_vulnerabilities: list | None = None,
    damage_resistances: list | None = None,
    damage_immunities: list | None = None,
    condition_immunities: list | None = None,
    senses: str = "",
    senses_passive: int = 10,
    languages: str = "",
    cr: float = 0,
    attacks: list | None = None,
    multiattack: str | None = None,
    legendary_actions: list | None = None,
    lair_actions: list | None = None,
) -> dict:
    spd_parts = [f"{speed} ft."]
    if fly_speed:
        spd_parts.append(f"fly {fly_speed} ft.")
    if swim_speed:
        spd_parts.append(f"swim {swim_speed} ft.")
    if climb_speed:
        spd_parts.append(f"climb {climb_speed} ft.")

    return {
        "name": name,
        "size": size,
        "type": creature_type,
        "alignment": alignment,
        "ac": ac,
        "hp": {"max": hp_max, "formula": hp_formula},
        "speed": ", ".join(spd_parts),
        "STR": STR, "DEX": DEX, "CON": CON,
        "INT": INT, "WIS": WIS, "CHA": CHA,
        "saving_throws": saving_throws or {},
        "skills": skills or {},
        "damage_resistances": damage_resistances or [],
        "damage_immunities": damage_immunities or [],
        "condition_immunities": condition_immunities or [],
        "senses": senses,
        "senses_passive": senses_passive,
        "languages": languages,
        "cr": cr,
        "xp": CR_XP.get(cr, 0),
        "attacks": attacks or [],
        "multiattack": multiattack,
        "legendary_actions": legendary_actions,
        "lair_actions": lair_actions,
    }


