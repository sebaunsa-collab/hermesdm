# ── Lookup Functions ──────────────────────────────────────────────────────

def get_monster(name: str) -> Optional[dict]:
    """Look up a monster by exact name key.

    Args:
        name: Monster key name (lowercase, snake_case), e.g. "goblin", "young_red_dragon"

    Returns:
        Monster stat block dict, or None if not found.
    """
    return MONSTER_MANUAL.get(name)


def get_monsters_by_cr(min_cr: float, max_cr: float) -> List[str]:
    """Return monster names whose CR falls within [min_cr, max_cr] inclusive.

    Args:
        min_cr: Minimum challenge rating (inclusive)
        max_cr: Maximum challenge rating (inclusive)

    Returns:
        List of monster name strings sorted alphabetically.
    """
    result = [
        name for name, monster in MONSTER_MANUAL.items()
        if min_cr <= monster["cr"] <= max_cr
    ]
    result.sort()
    return result
